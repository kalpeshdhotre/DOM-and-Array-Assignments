// Calculate the slope, x-intercept and y-intercept of y = 2x -2

// comparing this equation with ax+by+c=0
// we get  2x-y-2=0

// a=2 b=-1 c=-2

// slope of the line = -a/b = -2/1 = -2
// x-intercept = -c/a = -(-2)/2 = 1
// y-intercept = -c/b = -(-2)/-1 = -2

let a = 2;
let b = -1;
let c = -2;

let xIntercept = -c / a;
let yIntercept = -c / b;
let slope = -a / b;
// x-intercept = -c/a = -(-2)/2 = 1

console.log(`slope of line is ${slope}`);

console.log(`x-intercept is ${xIntercept}`);
console.log(`x-intercept is ${yIntercept}`);
